
public class Operaciones {

	public int suma(int a, int b) {
		return a+b;
	}//suma
	
	public int resta(int a, int b) {
		return a-b;
	}//resta
	
	public int multiplica(int a, int b) {
		return a*b;
	}//multiplica
	
	public int divide(int a, int b) {
		return a/b;
	}//divide
	
	public double getPI() {
		return 3.1415926;
	}
	
	public String getstrPI() {
		return "3.1415926";
	}
	
}
